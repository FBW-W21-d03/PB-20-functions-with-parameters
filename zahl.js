//1
function add(erstesumme,zweite,dritte) {
    console.log( "+:", erstesumme+zweite+dritte)
};
//2
function minus(erstesumme,zweite,dritte) {
    console.log( "-:", erstesumme-zweite-dritte)
};
//3
function multi(erstesumme,zweite,dritte) {
    console.log( "*:", erstesumme*zweite*dritte)
};
//4
function divi(erstesumme,zweite,dritte) {
    console.log("/:", erstesumme/zweite/dritte)
};

add(5,88,965);
minus(87,59,3);
multi(897,0,589);
divi(87,999,56);