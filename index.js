function addition(firstNumber, secondNumber, thirdNumber) {
    let additionSumme = firstNumber + secondNumber + thirdNumber;
    console.log("addition ist", additionSumme);
}

function subtraktion(firstNumber, secondNumber, thirdNumber) {
    let subtraktionSumme = firstNumber - secondNumber - thirdNumber;
    console.log("subtraktion ist", subtraktionSumme);
}

function multiplikation(firstNumber, secondNumber, thirdNumber) {
    let multiplikationSumme = firstNumber * secondNumber * thirdNumber;
    console.log("multiplikation ist", multiplikationSumme);
}

function division(firstNumber, secondNumber, thirdNumber) {
    let divisionSumme = firstNumber / secondNumber / thirdNumber;
    console.log("division ist", divisionSumme);
}

addition(5, 8, 2);
subtraktion(4, 6, 4);
multiplikation(8, 7, 7);
division(10, 6, 8);